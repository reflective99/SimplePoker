package main;

/**
 * The CardSuit contains all of the Suits of a 
 * regular deck of 52 playing cards. The Suits 
 * have no particular ordering or ranking unless 
 * we're dealing with a {@link Ranking#FLUSH Flush}.
 */
public enum CardSuit {
  
  /** Spade Suit */
  SPADE,
  /** Diamond Suit */
  DIAMOND,
  /** Club Suit */
  CLUB,
  /** Heart Suit */
  HEARTS,
  
}
